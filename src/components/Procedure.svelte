        <p>"Participants were informed they should sit down in a chair or, if they prefer, to lie down, and to get a timer with a seconds counter that they could watch.</p>
        <p>Then they were told to take the “CO2 tolerance test” as follows.</p>
        <ol>
            <li>Take 4 breaths. An inhale followed an exhale = 1 breath. Ideally these are all done via the nose.</li>
            <li>Then take a maximally deep breath and once your lungs are full, exhale as slowly as possible through your nose or mouth.</li>
            <li>Time how long it takes (in sec) to empty your lungs; this will be your C02 discard duration.</li>
            <li>Do not hold your breath with lungs empty. Once your lungs are empty simply record your ‘discard duration.</li>
            <li>Use your discard duration to determine how long your inhales, exhales, and breath holds should be for the box breathing protocol using this table:</li>
        </ol>
        <ul>
            <li>0–20 s C02 discard time = your inhales, exhales, and breath holds should be 3 - 4s.</li>
            <li>25–45 s C02 discard time = your inhales, exhales, and breath holds should be 5 - 6s.</li>
            <li>50 - 75 + sec C02 discard time = your inhales, exhales, and breath holds should be 8–10 s.</li>
        </ul>
        <p>Participants were informed they should sit down in a chair or, if they prefer, to lie down, and to set a timer for 5 min. They were told to then inhale (for the duration determined by the C02 discard rate lookup table), then to hold their breath for the equivalent duration, then to exhale for the same duration and then to hold their breath for again, the same duration (e.g. inhale 4 s, hold 4 s, exhale 4 s, hold 4 s) and to repeat this pattern for the entire 5 min. They were told that if at any point they had to strain to reach these times, they should simply reduce the duration of inhales, exhales, and breath holds. We asked participants to perform all breathing through their nose, if possible, but that if they felt the need to switch to breathing through their mouth, to do so.</p>
        <p>Then they were told to return to breathing normally."</p>
